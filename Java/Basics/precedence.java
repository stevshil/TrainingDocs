class precedence {
    public static void main(String[] args) {
        System.out.println ("The interest amount is =" + 100+25*0.20/2);
        //  Watch out!
        // The maths is 25*0.2 first, then divide by 2
        // The + is concatenation not addition so 100 + 2.5 = 1002.5
        System.out.println("The result is: " + 20+1*5.0/2);
    }
}